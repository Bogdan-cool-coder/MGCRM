_NON_PHONE_RE = re.compile(r"[^\d+]")


def _normalize_phone_for_prefix(phone: str) -> str:
    """Приводим телефон к виду '+<digits>' для матчинга префикса.

    - "8 (701) 234-56-78"   → "+87012345678"  (но 8 не префикс → не сматчится)
    - "+7 701 234 56 78"    → "+77012345678"
    - "00971 5..."          → "+9715..."  (00 → +)
    - "WhatsApp: +44…"      → "+44..."
    """
    if not phone:
        return ""
    # Убираем всё кроме цифр и +
    cleaned = _NON_PHONE_RE.sub("", phone)
    if not cleaned:
        return ""
    # Нормализуем международный префикс 00 → +
    if cleaned.startswith("00"):
        cleaned = "+" + cleaned[2:]
    # Если нет ведущего + и начинается с 8 (RU/KZ старый формат) — НЕ
    # додумываем, возвращаем как есть. Пусть менеджер дозаполнит руками.
    return cleaned


def infer_country_from_phone(phone: str | None) -> str | None:
    """Возвращает ISO-2 код страны по телефонному префиксу. None если не
    определилось (нет префикса, пустая строка, мусор, неизвестный префикс).

    Алгоритм: жадный match — пытаемся самые длинные префиксы первыми
    (4 знака → 3 → 2). Это гарантирует, что `+998` (UZ) перебьёт несуществующий
    `+99`, и `+995` (GE) перебьёт несуществующий `+9`.
    """
    if not phone:
        return None
    normalized = _normalize_phone_for_prefix(phone)
    if not normalized.startswith("+"):
        return None

    # Сортируем префиксы от длинных к коротким — жадный match.
    sorted_prefixes = sorted(
        PHONE_PREFIX_TO_COUNTRY.keys(), key=len, reverse=True
    )
    for prefix in sorted_prefixes:
        if normalized.startswith(prefix):
            return PHONE_PREFIX_TO_COUNTRY[prefix]
    return None
